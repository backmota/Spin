#import "MisPiscinasTVC.h"
#import "SWRevealViewController.h"

@interface MisPiscinasTVC (){
    NSMutableArray *listaPiscinas;
}

@end

@implementation MisPiscinasTVC

- (void)viewDidLoad {
    [super viewDidLoad];
    
    SWRevealViewController *revealViewController = self.revealViewController;
    if ( revealViewController )
    {
        [self.sidebarButton setTarget: self.revealViewController];
        [self.sidebarButton setAction: @selector( revealToggle: )];
        [self.view addGestureRecognizer:self.revealViewController.panGestureRecognizer];
    }
    
    listaPiscinas = [[NSMutableArray alloc] initWithObjects:@"uno", nil];
    
    [self.view setBackgroundColor:[UIColor colorWithRed:0.36 green:0.65 blue:0.8 alpha:1]];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [listaPiscinas count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    static NSString *simpleTableIdentifier = @"cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:simpleTableIdentifier];
    
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:simpleTableIdentifier];
    }
    
    [cell setBackgroundColor:[UIColor colorWithRed:0.36 green:0.65 blue:0.8 alpha:1]];
    cell.textLabel.textColor = [UIColor whiteColor];
    
    cell.textLabel.text = [listaPiscinas objectAtIndex:indexPath.row];
    return cell;
}


